  static const Color warningLight = Color(0xFFE76F51); // Coral red
  static const Color accentLight =
      Color(0xFF8B5CF6); // Purple for premium features
  static const Color backgroundLight = Color(0xFFFEFEFE); // Near-white
  static const Color surfaceLight = Color(0xFFF8F9FA); // Light gray for cards
  static const Color errorLight = Color(0xFFE76F51); // Coral red for errors
  static const Color onPrimaryLight = Color(0xFFFFFFFF);
  static const Color onSecondaryLight = Color(0xFF1A1A1A);
  static const Color onBackgroundLight = Color(0xFF1A1A1A); // Near-black text
