  static const Color textSecondaryLight = Color(0xFF6B7280); // Medium gray
  static const Color textDisabledLight = Color(0xFFE5E7EB); // Light gray
  static const Color borderLight = Color(0xFFE5E7EB); // Subtle gray borders

