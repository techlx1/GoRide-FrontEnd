  static const Color secondaryDark = Color(0xFFF4A261); // Same warm amber
  static const Color secondaryVariantDark = Color(0xFFE09142); // Darker amber
  static const Color successDark = Color(0xFF4A7C59); // Lighter forest green
  static const Color warningDark = Color(0xFFE76F51); // Same coral red
  static const Color accentDark = Color(0xFF8B5CF6); // Same purple
  static const Color backgroundDark =
